# Python Data Types & Variables

# Number - integers, float (decimal numbers or fractions)
# String - is a character or collection of characters, basically used for text data
# Collections - kind of data types which are collections or group of other data types
# - List - like array, collection of other data types, denoted by []
# - Tuple - like array, collection of other data types, denoted by ()
#         - Special kind of list, in which modification of data or elements is not allowed  
# - Set - like list only, but duplicates are not allowed , denoted by {}
# - Dictionary - special type of collection, which uses mapping of key and values
#              - works like key : value pair
#              - "class" : "Xth class"
#              - denoted by { "key" : "value" , "key2" : "value"}


