import math

version = "1.0"

def add_numbers(num1,num2):
    return num1 + num2

def multiply(num1,num2):
    return num1 * num2

def power_of(num1,num2):
    return num1 ** num2

def remainder_of(num1,num2):
    return num1 % num2

def sine_theta(angle_value):
    return math.sin(angle_value)

def cos_theta(angle_value):
    return math.cos(angle_value)